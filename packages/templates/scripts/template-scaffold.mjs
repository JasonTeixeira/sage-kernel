import fs from "node:fs";
import path from "node:path";

const args = process.argv.slice(2);

function argValue(name) {
  const index = args.indexOf(`--${name}`);
  return index >= 0 ? args[index + 1] : null;
}

function slugify(value) {
  return value
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

const templateId = argValue("template");
const rawName = argValue("name");
const outDir = argValue("out") || "generated";

if (!templateId || !rawName) {
  console.error("Usage: npm run template:scaffold -- --template <id> --name <project-name> [--out generated]");
  process.exit(1);
}

const root = process.cwd();
const templatesPath = path.join(root, "catalog", "templates.json");
const qaProfilesPath = path.join(root, "packages", "qa", "profiles.json");
const modulesPath = path.join(root, "catalog", "modules.json");
const integrationsPath = path.join(root, "catalog", "integrations.json");

const { templates } = JSON.parse(fs.readFileSync(templatesPath, "utf8"));
const { profiles } = JSON.parse(fs.readFileSync(qaProfilesPath, "utf8"));
const { modules } = JSON.parse(fs.readFileSync(modulesPath, "utf8"));
const { integrations } = JSON.parse(fs.readFileSync(integrationsPath, "utf8"));

const template = templates.find((item) => item.id === templateId);
if (!template) {
  throw new Error(`Unknown template: ${templateId}`);
}

const qaProfile = profiles.find((item) => item.id === template.qaProfile);
if (!qaProfile) {
  throw new Error(`Template ${templateId} references missing QA profile: ${template.qaProfile}`);
}

const projectName = slugify(rawName);
const projectDir = path.resolve(root, outDir, projectName);

if (fs.existsSync(projectDir)) {
  throw new Error(`Refusing to overwrite existing project directory: ${projectDir}`);
}

fs.mkdirSync(path.join(projectDir, "docs"), { recursive: true });
fs.mkdirSync(path.join(projectDir, "src"), { recursive: true });
fs.mkdirSync(path.join(projectDir, ".sage"), { recursive: true });

const relevantModules = modules
  .filter((module) =>
    template.coverage.some((capability) =>
      module.responsibilities.some((responsibility) => responsibility.includes(capability))
    )
  )
  .map((module) => module.id);

const plan = {
  name: projectName,
  template: template.id,
  stack: template.defaultStack,
  coverage: template.coverage,
  qaProfile: qaProfile.id,
  qaModes: {
    fast: qaProfile.fast,
    standard: qaProfile.standard,
    thorough: qaProfile.thorough,
    deep: qaProfile.deep
  },
  hardBlockers: qaProfile.hardBlockers,
  suggestedKernelModules: relevantModules,
  suggestedIntegrations: integrations
    .filter((integration) =>
      template.coverage.some((capability) =>
        integration.capabilities.some((integrationCapability) => integrationCapability.includes(capability))
      )
    )
    .map((integration) => integration.id)
};

fs.writeFileSync(
  path.join(projectDir, ".sage", "project-plan.json"),
  `${JSON.stringify(plan, null, 2)}\n`
);

fs.writeFileSync(
  path.join(projectDir, "README.md"),
  `# ${projectName}

Generated by Sage Kernel OS.

## Template

${template.id}

## Default Stack

${template.defaultStack.map((item) => `- ${item}`).join("\n")}

## Coverage

${template.coverage.map((item) => `- ${item}`).join("\n")}

## QA Profile

${qaProfile.id}

Fast runners:

${qaProfile.fast.map((item) => `- ${item}`).join("\n")}

Standard runners:

${qaProfile.standard.map((item) => `- ${item}`).join("\n")}

## Next Steps

1. Replace placeholder source files with the concrete app implementation.
2. Add environment variables in \`.env.example\`.
3. Run the matching QA OS profile before deploy.
4. Register the repo in Sage Kernel catalog after first commit.
`
);

fs.writeFileSync(
  path.join(projectDir, ".env.example"),
  "# Add required environment variables here.\n"
);

fs.writeFileSync(
  path.join(projectDir, "src", "index.md"),
  `# ${projectName} Source Placeholder

This placeholder exists so the Phase 4 scaffold has a durable filesystem contract.
Future template hardening will replace this with framework-specific source code.
`
);

fs.writeFileSync(
  path.join(projectDir, "docs", "architecture.md"),
  `# Architecture

## Template

${template.id}

## Kernel Plan

See \`.sage/project-plan.json\`.
`
);

console.log(`Scaffolded ${projectName}`);
console.log(projectDir);
