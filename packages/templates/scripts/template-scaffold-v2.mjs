import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";

const args = process.argv.slice(2);
const arg = (name) => {
  const index = args.indexOf(`--${name}`);
  return index >= 0 ? args[index + 1] : null;
};
const slugify = (value) => value.toLowerCase().trim().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
const write = (file, content) => {
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, content);
};
const writeJson = (file, value) => write(file, `${JSON.stringify(value, null, 2)}\n`);

const templateId = arg("template");
const rawName = arg("name");
const outDir = arg("out") || "generated";
if (!templateId || !rawName) {
  console.error("Usage: npm run template:scaffold -- --template <id> --name <project-name> [--out generated]");
  process.exit(1);
}

const root = process.cwd();
const { templates } = JSON.parse(fs.readFileSync(path.join(root, "catalog/templates.json"), "utf8"));
const { profiles } = JSON.parse(fs.readFileSync(path.join(root, "packages/qa/profiles.json"), "utf8"));
const template = templates.find((item) => item.id === templateId);
if (!template) throw new Error(`Unknown template: ${templateId}`);
const qaProfile = profiles.find((item) => item.id === template.qaProfile);
if (!qaProfile) throw new Error(`Missing QA profile: ${template.qaProfile}`);

const projectName = slugify(rawName);
const projectDir = path.resolve(root, outDir, projectName);
if (fs.existsSync(projectDir)) throw new Error(`Refusing to overwrite existing project directory: ${projectDir}`);

fs.mkdirSync(projectDir, { recursive: true });
writeJson(path.join(projectDir, ".sage/project-plan.json"), {
  name: projectName,
  template: template.id,
  stack: template.defaultStack,
  coverage: template.coverage,
  qaProfile: qaProfile.id,
  qaModes: { fast: qaProfile.fast, standard: qaProfile.standard, thorough: qaProfile.thorough, deep: qaProfile.deep },
  hardBlockers: qaProfile.hardBlockers,
  productionReadiness: {
    docker: true,
    ci: true,
    envValidation: true,
    healthCheck: true,
    runbook: true
  }
});
writeCommon(projectDir, projectName, template, qaProfile);

const emitters = {
  "next-saas-app": emitNextSaas,
  "next-ai-app": emitNextAi,
  "fastapi-service": emitFastApi,
  "worker-service": emitWorkerService,
  "expo-mobile-app": emitExpoMobile,
  "node-api-service": emitNodeApi,
  "next-dashboard": emitNextDashboard,
  "agent-workflow-app": emitAgentWorkflow
};
(emitters[template.id] || emitGeneric)(projectDir, projectName);

spawnSync("git", ["init"], { cwd: projectDir, encoding: "utf8" });
write(path.join(projectDir, ".gitignore"), "node_modules/\n.next/\ndist/\n.env\n.env.*\n!.env.example\n.DS_Store\n");

console.log(`Scaffolded ${projectName}`);
console.log(projectDir);

